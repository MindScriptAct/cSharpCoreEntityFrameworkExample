using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using AutoMapper;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using UniversityMvcProject.Data;
using UniversityMvcProject.Data.Contexts;
using UniversityMvcProject.Data.Models;
using UniversityMvcProject.Models;
using UniversityMvcProject.ViewModels;

namespace UniversityMvcProject.Controllers
{
    public class StudentAdminController : Controller
    {
        private readonly UniversityMvcProjectContext _context;
        private readonly IMapper mapper;

        public StudentAdminController(UniversityMvcProjectContext context, IMapper mapper)
        {
            _context = context;
            this.mapper = mapper;
        }

        // GET: StudentAdmin
        public async Task<IActionResult> Index()
        {
            return View(await _context.Students.Include(s => s.Dormitory).ToListAsync());
        }

        // GET: StudentAdmin/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var student = await _context.Students
                .FirstOrDefaultAsync(m => m.Id == id);
            if (student == null)
            {
                return NotFound();
            }

            var subjects = await _context.StudentSubjects.Where(ss => ss.StudentId == id).Select(ss => ss.Subject).ToListAsync();




            var viewModel = mapper.Map<StudentDetailViewModel>(student);
            viewModel.Subjects = subjects;



            return View(viewModel);
        }

        // GET: StudentAdmin/Create
        public IActionResult Create()
        {
            ViewBag.DormitoryId = new SelectList(_context.Dormitories, "Id", "Streat");
            return View();
        }

        // POST: StudentAdmin/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("Id,Name,Score,FriendCount,IsGettingTuition,Status,BirthDay")] Student student)
        {
            if (ModelState.IsValid)
            {
                _context.Add(student);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            return View(student);
        }

        // GET: StudentAdmin/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var student = await _context.Students.FindAsync(id);
            if (student == null)
            {
                return NotFound();
            }
            return View(student);
        }

        // POST: StudentAdmin/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("Id,Name,Score,FriendCount,IsGettingTuition,Status,BirthDay")] Student student)
        {
            if (id != student.Id)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(student);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!StudentExists(student.Id))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(student);
        }

        // GET: StudentAdmin/Delete/5
        public async Task<IActionResult> Delete(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var student = await _context.Students
                .FirstOrDefaultAsync(m => m.Id == id);
            if (student == null)
            {
                return NotFound();
            }

            return View(student);
        }

        // POST: StudentAdmin/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(int id)
        {
            var student = await _context.Students.FindAsync(id);
            _context.Students.Remove(student);
            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Index));
        }

        private bool StudentExists(int id)
        {
            return _context.Students.Any(e => e.Id == id);
        }
    }
}
